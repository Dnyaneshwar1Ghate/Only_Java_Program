package Simple_Java_Programs_Sanfoundry;

public class Swap_Integers {
	
	public static void main(String[] args) {
		
		int num1=10;
		int num2=60;
		
		System.out.println("Before swap"+num1+" "+num2);;
		int temp=num1;
		num1=num2;
		num2=temp;
		System.out.println("after swap "+num1+" "+num2);
		
		
	}

}
